/**
 * search-demo.js
 * Временный обработчик для кнопки «Найти» в основном калькуляторе.
 * В будущем здесь будет отправка AJAX-запроса к API обменников.
 */

// Ждём полной загрузки DOM, чтобы кнопка точно существовала
document.addEventListener('DOMContentLoaded', () => {
  // Находим кнопку поиска по её идентификатору
  const searchBtn = document.getElementById('searchBtn');

  // Проверяем, есть ли такая кнопка на странице
  if (searchBtn) {
    // Назначаем обработчик клика
    searchBtn.addEventListener('click', () => {
      // Временное действие: показываем сообщение-заглушку
      alert('Поиск обменников (демо).\nЗдесь будет отправка запроса с параметрами из формы.');
    });
  }
});